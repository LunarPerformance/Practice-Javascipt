// switch case
let x = 5;
switch (x) {
    case 2:
        console.log("Thứ Hai");
        break;
    case 3:
        console.log("Thứ Ba");
        break;
    case 4:
        console.log("Thứ Tư");
        break;
    case 5:
        console.log("Thứ Năm");
        break;
    default:
        console.log("Giá trị không hợp lệ");
}